/**
 * @file std_types.h
 * @brief This header file defines standard types.
 *
 * This file contains type definitions for Data types of various sizes.
 * It can be included in any project where these standard types are needed.
 *
 * @author Mustafa Mohamed
 * @date August 13, 2024
 */

#ifndef COMMON_INC_STD_TYPES_H_
#define COMMON_INC_STD_TYPES_H_

/**
 * @typedef uint8
 * @brief Defines an unsigned 8-bit integer.
 *
 * This type is used to represent an unsigned integer
 * that occupies 8 bits (1 byte) of memory.
 */
typedef unsigned char  uint8;

/**
 * @typedef uint16
 * @brief Defines an unsigned 16-bit integer.
 *
 * This type is used to represent an unsigned integer
 * that occupies 16 bits (2 bytes) of memory.
 */
typedef unsigned short uint16;

/**
 * @typedef uint32
 * @brief Defines an unsigned 32-bit integer.
 *
 * This type is used to represent an unsigned integer
 * that occupies 32 bits (4 bytes) of memory.
 */
typedef unsigned int   uint32;

#endif /* COMMON_INC_STD_TYPES_H_ */
